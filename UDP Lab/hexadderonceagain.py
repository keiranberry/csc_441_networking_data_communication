# Values in hex
values = [
    0xC0A8, 0x0165, 0x4457, 0x47E2,
    0x0000, 0x0011, 0x0025, 0x1117,
    0x0035, 0x0025, 0x0000, 
    0x0000, 0x0401, 0x0000,
    0x0100, 0x0000, 0x0000,
    0x0003, 0x7777, 0x7703,
    0x6D69, 0x7403, 0x6564,
    0x7500, 0x0001, 0x0001
]

# Calculate the sum
total = sum(values)

# Handle overflow
while total >> 16:
    total = (total & 0xFFFF) + (total >> 16)

# One's complement
checksum = ~total & 0xFFFF
checksum_hex = f"{checksum:04X}"
print(checksum_hex)
